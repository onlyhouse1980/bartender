export function translateAmount(amount: string, lang: SupportedLanguage): string {
  let result = amount;
  const map = ingredientAmountTranslations[lang] || ingredientAmountTranslations['en'];
  if (map) {
    for (const [key, value] of Object.entries(map)) {
      if (result.includes(key)) {
        result = result.replace(key, value);
      }
    }
  }
  return result;
}

