export function getLocalizedImportedDrinks(importedDrinks: readonly Drink[], lang: SupportedLanguage): readonly Drink[] {
  if (lang === 'de') return importedDrinks;
  return importedDrinks.map((drink) => {
    const localizedCategory = translateCategory(drink.category, lang);
    const localizedGlass = translateGlassware(drink.glass, lang);
    const localizedGarnish = translateGarnish(drink.garnish, lang);
    const localizedIngredients = drink.ingredients.map((ing: any) => ({
      amount: translateAmount(ing.amount, lang),
      item: translateIngredient(ing.item, lang),
    }));
    
    let summary = drink.summary;
    let germanNote = drink.germanNote;
    let proTip = drink.proTip;
    
    if (drink.source === 'web-import') {
      if (lang === 'en') {
        summary = `Drink imported from the web, category ${localizedCategory}. Ingredients and instructions are from TheCocktailDB.`;
        germanNote = 'Imported via web search. Online recipes may differ in measurements, glassware, and garnish depending on the source.';
        proTip = 'Briefly compare the imported recipe with your bar standard before using it in service.';
      } else if (lang === 'fr') {
        summary = `Boisson importée du web, catégorie ${localizedCategory}. Les ingrédients et instructions proviennent de TheCocktailDB.`;
        germanNote = 'Importé via recherche web. Les recettes en ligne peuvent différer au niveau des mesures, des verres et de la garniture selon la source.';
        proTip = 'Comparez brièvement la recette importée avec vos standards de bar avant de l\'utiliser en service.';
      } else if (lang === 'es') {
        summary = `Bebida importada de la web, categoría ${localizedCategory}. Los ingredientes y las instrucciones son de TheCocktailDB.`;
        germanNote = 'Importado mediante búsqueda web. Las recetas en línea pueden variar en medidas, cristalería y guarnición según la fuente.';
        proTip = 'Compara brevemente la receta importada con el estándar de tu bar antes de usarla en el servicio.';
      } else if (lang === 'nl') {
        summary = `Drankje geïmporteerd van het web, categorie ${localizedCategory}. Ingrediënten en instructies zijn afkomstig van TheCocktailDB.`;
        germanNote = 'Geïmporteerd via webzoeker. Online recepten kunnen verschillen in afmetingen, glaswerk en garnering, afhankelijk van de bron.';
        proTip = 'Vergelijk het geïmporteerde recept kort met je barstandaard voordat je het in de service gebruikt.';
      }
    } else if (drink.source === 'custom' || !drink.source) {
      const iceMatch = drink.summary.match(/Eis:\s*([^.]+)/);
      const ice = iceMatch ? iceMatch[1].trim() : 'Eiswürfel';
      const translatedIce = translateIngredient(ice, lang);
      const translatedTechnique = techniqueTranslations[lang]?.[drink.technique] || drink.technique;
      
      if (lang === 'en') {
        summary = `Custom drink for local library. Ice: ${translatedIce}. Technique: ${translatedTechnique}.`;
        germanNote = 'Locally created drink. Recipe, glassware, ice, and image remain saved on this device.';
        proTip = `Plan for ${translatedIce.toLowerCase()} during service and follow the house standard for ${localizedGlass}.`;
      } else if (lang === 'fr') {
        summary = `Boisson personnalisée pour la bibliothèque locale. Glace : ${translatedIce}. Technique : ${translatedTechnique}.`;
        germanNote = 'Boisson créée localement. La recette, le verre, la glace et l\'image restent enregistrés sur cet appareil.';
        proTip = `Prévoyez de la ${translatedIce.toLowerCase()} pour le service et respectez les standards de la maison pour le ${localizedGlass}.`;
      } else if (lang === 'es') {
        summary = `Bebida personalizada para la biblioteca local. Hielo: ${translatedIce}. Técnica: ${translatedTechnique}.`;
        germanNote = 'Bebida creada localmente. La receta, el vaso, el hielo y la imagen permanecen guardados en este dispositivo.';
        proTip = `Planifica el uso de ${translatedIce.toLowerCase()} durante el servicio y sigue el estándar de la casa para ${localizedGlass}.`;
      } else if (lang === 'nl') {
        summary = `Eigen drankje voor lokale bibliotheek. IJs: ${translatedIce}. Techniek: ${translatedTechnique}.`;
        germanNote = 'Lokaal gemaakt drankje. Recept, glas, ijs en afbeelding blijven opgeslagen op dit apparaat.';
        proTip = `Houd rekening met ${translatedIce.toLowerCase()} tijdens de service en volg de huisstandaard voor ${localizedGlass}.`;
      }
    }

    return {
      ...drink,
      category: localizedCategory,
      glass: localizedGlass,
      garnish: localizedGarnish,
      ingredients: localizedIngredients,
      summary,
      germanNote,
      proTip,
    };
  });
}


