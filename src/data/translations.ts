export function getLocalizedDrinks(deDrinks: readonly Drink[], lang: SupportedLanguage): readonly Drink[] {
  if (lang === 'de') {
    return deDrinks;
  }
  return deDrinks.map((drink) => {
    const translation = drinkDescriptionsTranslations[lang as Exclude<SupportedLanguage, 'de'>]?.[drink.id] || drinkDescriptionsTranslations['en']?.[drink.id];
    
    const localizedIngredients = drink.ingredients.map((ing: any) => {
      return {
        amount: translateAmount(ing.amount, lang),
        item: translateIngredient(ing.item, lang),
      };
    });

    if (!translation) {
      return {
        ...drink,
        category: translateCategory(drink.category, lang),
        glass: translateGlassware(drink.glass, lang),
        garnish: translateGarnish(drink.garnish, lang),
        ingredients: localizedIngredients,
      };
    }

    return {
      ...drink,
      category: translateCategory(drink.category, lang),
      glass: translateGlassware(drink.glass, lang),
      garnish: translateGarnish(drink.garnish, lang),
      summary: translation.summary || drink.summary,
      germanNote: translation.germanNote || drink.germanNote,
      proTip: translation.proTip || drink.proTip,
      ingredients: localizedIngredients,
      method: translation.method || drink.method,
    };
  });
}

